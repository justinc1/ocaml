let size_ref = ref None

let set_size (new_size : [ `Thirty_two | `Sixty_four ]) =
  size_ref := Some new_size

let size () =
  match !size_ref with
  | None -> failwith "Dwarf_format.set_size has not been called"
  | Some size -> size
