type t =
  | Thirty_two of Int32.t
  | Sixty_four of Int64.t

let parse ~stream : t =
  match Stream.dwarf_format stream with
  | Thirty_two ->
    Stream.read_int32 stream
    >>= fun i ->
    Thirty_two i
  | Sixty_four ->
    Stream.read_int64 stream
    >>= fun i ->
    Sixty_four i
